package com.haojie.pojo;

public class DatePojo {
	private int yyyy;
	private int mm;
	private int dd;
	private int hh;
	private int MM;
	private int ss;
	
	
	public int getMm() {
		return mm;
	}
	public void setMm(int mm) {
		this.mm = mm;
	}
	public int getHh() {
		return hh;
	}
	public void setHh(int hh) {
		this.hh = hh;
	}
	public int getMM() {
		return MM;
	}
	public void setMM(int mM) {
		MM = mM;
	}
	public int getSs() {
		return ss;
	}
	public void setSs(int ss) {
		this.ss = ss;
	}
	
	public void setYyyy(int yyyy) {
		this.yyyy = yyyy;
	}
	public int getYyyy() {
		return yyyy;
	}
	public void setDd(int dd) {
		this.dd = dd;
	}
	public int getDd() {
		return dd;
	}
}
